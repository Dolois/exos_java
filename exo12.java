public class exo12 {

	public static void main(String[] args) {
		int a = 74;
		int b = 36;
		int c = 5;
		int d = 10;
		int e = 15;
		int f = 10;
		int sum = 0;
		sum = (a + b + c + d + e + f);

		System.out.println(a + " + " + b + " + " + c + " + " + d + " + " + e + " + " + f + " = " + sum);
    }
}